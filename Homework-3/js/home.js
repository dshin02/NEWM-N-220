// Using const (Preferred for constant values)
// Using let (Preferred for variable changes)

const radius = 5; // Constant because the radius does not change.

const area = Math.PI * radius * radius; // Constant because the formula for the area of a circle is fixed.

const perimeter = 2 * Math.PI * radius; // Constant because the formula for the perimeter of a circle is fixed.

console.log("Area: " + area);

console.log("Perimeter: " + perimeter);



