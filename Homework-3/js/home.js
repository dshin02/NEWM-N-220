// Using const (Preferred for constant values)

const radius = 5; // Constant because the radius does not change.

const area = Math.PI * Math.pow(radius, 2); // Using Math.PI for PI and Math.pow for squaring the radius. Had to look up Math.pow for squaring the radius.

const perimeter = 2 * Math.PI * radius;

console.log("Area: " + area);

console.log("Perimeter: " + perimeter);



